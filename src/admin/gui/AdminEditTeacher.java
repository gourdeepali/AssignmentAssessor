    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        String name;
        try {
            name = admindao.searchteacher(searchfield.getText());
            namelabel.setText(name);
            addbtn.setEnabled(true);
            removebtn.setEnabled(true);
            loadsubjects(searchfield.getText());

        } catch (SQLException ex) {
            Logger.getLogger(AdminEditTeacher.class.getName()).log(Level.SEVERE, null, ex);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_searchbtnActionPerformed

    private void removebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removebtnActionPerformed
        if (this.sublist.getSelectedIndices().length > 0) {
            int[] selectedIndices = sublist.getSelectedIndices();
            for (int i = selectedIndices.length - 1; i >= 0; i--) {
                try {
                    admindao.removesub(listModel.getElementAt(selectedIndices[i]).toString(), searchfield.getText());
                } catch (SQLException ex) {
                    Logger.getLogger(AdminEditTeacher.class.getName()).log(Level.SEVERE, null, ex);
                }
                listModel.removeElementAt(selectedIndices[i]);

            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_removebtnActionPerformed

