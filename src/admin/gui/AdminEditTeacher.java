public void loadsubjects(String tid) throws SQLException {
        listModel.removeAllElements();
        ArrayList<String> l = admindao.getTeacherSubjects(tid);
        l.forEach((item) -> {
            listModel.addElement(item);
        });
        sublist.setModel(listModel);
    }
